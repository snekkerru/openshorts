export const DICTS = { ru: RU };

export function translate(lang, key, vars) {
  let out = (lang !== 'en' && DICTS[lang] && DICTS[lang][key]) || key;
  if (vars) {
    for (const k of Object.keys(vars)) {
      out = out.replaceAll(`{${k}}`, vars[k]);
    }
  }
  return out;
}
